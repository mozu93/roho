def to_halfwidth_kana(text: str) -> str:
    """ひらがな/全角カタカナ → 半角カタカナ、全角スペース → 半角スペース"""
    result = []
    for ch in text:
        code = ord(ch)
        if 0x3041 <= code <= 0x3096:  # ひらがな → 全角カタカナ
            ch = chr(code + 0x60)
        if ord(ch) == 0x3000:  # 全角スペース → 半角
            ch = ' '
        result.append(_KANA_HW_MAP.get(ch, ch))
    return ''.join(result)
